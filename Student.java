class Student {
    private int Student_Id;
    private String Name;
    
    public void setId(int id){
        this.Student_Id = id;
    }
    public int getId(){
        return Student_Id;
    }
    public void setname(String s_name){
        this.Name = s_name;
    }
    public String getname(){
        return Name;
    }
    }
    
